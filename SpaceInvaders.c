void Delay100ms(unsigned long count)	{
	unsigned long volatile time;
  while(count>0){
    time = 727240;  // 0.1sec at 80 MHz
    while(time){
	  	time--;
    }
    count--;
  }
}


unsigned long FrameCount=0;
void Draw_BMP(int size){ 
	int i;
	Nokia5110_ClearBuffer();
  //Draw Enemies 
	for(i=0;i<size;i++)
	{
		if(Enemy[i].life==1 && FrameCount <5)
			Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image[0], 0);
		else if(Enemy[i].life==1 && FrameCount<10)
			Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image[1], 0);
		else if(Enemy[i].Is_Bomb==0x00000001 && Enemy[i].Bomb_Counter>0)
		{
			Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y,SmallExplosion0, 0);
			Enemy[i].Bomb_Counter--;
			if(Enemy[i].Bomb_Counter==0)
				Enemy[i].Is_Bomb=0;
		}				
  }
	//Draw Fast Enemy
	if(FS.life==1 && FS.Free_Space==1)
		Nokia5110_PrintBMP(FS.x, FS.y, FS.Image, 0);
	else if(FS.Is_Bomb==1 && FS.Bomb_Counter >0)
	{
		Nokia5110_PrintBMP(FS.x, FS.y,BigExplosion0, 0);		
		FS.Bomb_Counter--;
		if(FS.Bomb_Counter==0)
			FS.Is_Bomb=0;
	}
	//Draw PlayerShip
  Nokia5110_PrintBMP(PS.x, PS.y, PS.Image, 0);
  //Draw Bunker
	if(BK.life==1)
		Nokia5110_PrintBMP(BK.x,BK.y,BK.Image, 0);
	//Draw Lasers
	for(i=0;i<MAX_Lasers;i++)
	{
		if(Lasers[i].life==1)
			Nokia5110_PrintBMP(Lasers[i].x,Lasers[i].y, Lasers[i].Image, 0);
	}
	//Draw Missiles
	for(i=0;i<MAX_Missiles;i++)
	{	
		if(Missiles[i].life==1 )
			Nokia5110_PrintBMP(Missiles[i].x,Missiles[i].y, Missiles[i].Image, 0);	
	}
	// Display the whole buffer
  Nokia5110_DisplayBuffer();
  FrameCount = (FrameCount+1)%11; 

}
//Add new Laser
void Add_Laser(int LS_Size)
{
	int i;
	if(SW==1 && LastSW==0)
	{
		for(i=0;i<LS_Size;i++)
		{
			if(Lasers[i].life==0)
			{
				Lasers[i].life=1;
				Lasers[i].x=PS.x+PLAYERW/2;
				Lasers[i].y=47;
				Sound_Shoot();
				break;
			}
		}
	}
}
//Add New Missile
void Add_Missile(unsigned long Ran,unsigned long Add,int MS_Size)
{
	int i;
	if(Add==20 && Enemy[Ran].life==1)
	{
		for(i=0;i<MS_Size;i++)
		{
			if(Missiles[i].life==0)
			{
				Missiles[i].x=Enemy[Ran].x+ENEMY10W/2;
				Missiles[i].y=Enemy[Ran].y+8;
				Missiles[i].life=1;
				
				break;
			}
		}
	}
}

void Move_Missile(void)
{
	int i;	
		for(i=0;i<MAX_Missiles;i++)
		{
			if(Missiles[i].life==1)
			{
				Missiles[i].y+=1;
			}
			if(Missiles[i].y>=47)
				Missiles[i].life=0;
		}
}

void Move_Laser(void)
{
	int i;
	for(i=0;i<MAX_Lasers;i++)
	{
		if(Lasers[i].life==1)
			Lasers[i].y-=2;
		if(Lasers[i].y<=2)
			Lasers[i].life=0;
	}
}

void Check_Bonus(void)
{
	if(FS.life==1)
	{
		//Check if The first row Enemies are dead to allow the fast enemy to go
		if(Enemy[0].life ==0 && Enemy[0].Is_Bomb ==0&&Enemy[1].life ==0 && Enemy[1].Is_Bomb ==0&&Enemy[2].life ==0 && Enemy[2].Is_Bomb ==0&&Enemy[3].life ==0 && Enemy[3].Is_Bomb ==0)
			FS.Free_Space=1;
		//check if the first row enemies Position is allow or not to make the fast enemy go
		else if(Enemy[0].y >= (8+ENEMY10H) && Enemy[1].y >= (8+ENEMY10H) && Enemy[2].y >= (8+ENEMY10H) && Enemy[3].y >= (8+ENEMY10H))
			FS.Free_Space=1;
	}
}
// Move Function of fast enemy
void Move_Bonus()
{
	if(FS.life==1 && FS.Free_Space==1)
	{
		FS.x=(FS.x+2)%82;
		Sound_Fastinvader1();
	}
}

unsigned long t=0;
